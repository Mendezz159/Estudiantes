    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        int NumeroIngreso = Integer.parseInt(txtfillBuscarCodigo.getText());
        
        if(GestionEstudiantes.getMapa().containsKey(NumeroIngreso)){
            switch (tipo){
            case 1:
                ModificarEstudiante ventanaM = new ModificarEstudiante(NumeroIngreso, GestionEstudiantes.getMapa().get(NumeroIngreso));
                ventanaM.setVisible(true);
                break;
            case 2:
                Consultar ventanaC = new Consultar(NumeroIngreso, GestionEstudiantes.getMapa().get(NumeroIngreso));
                ventanaC.setVisible(true);
                break;
            case 3:
                AdvertenciaBorrar ventanaB = new AdvertenciaBorrar(NumeroIngreso, GestionEstudiantes.getMapa().get(NumeroIngreso));
                ventanaB.setVisible(true);
                break;
            }
            dispose();
            ModeloListaCodigos.removeAllElements();
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

