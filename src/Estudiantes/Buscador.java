    private void txtfillBuscarCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfillBuscarCodigoKeyTyped
        char i = evt.getKeyChar();
        
        if (!Character.isDigit(i) || txtfillBuscarCodigo.getText().length()==6){
            evt.consume();
        }else{
            RefrescarListaCodigosTyped(i);
        }
    }//GEN-LAST:event_txtfillBuscarCodigoKeyTyped

    private void txtfillBuscarCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfillBuscarCodigoKeyPressed
        char i = evt.getKeyChar();
        
        if (i=='\b'){
            RefrescarListaCodigosDeled();
        }
    }//GEN-LAST:event_txtfillBuscarCodigoKeyPressed

    private void RefrescarListaCodigosTyped(char c) {
        
        int numeroEscrito = Integer.parseInt(txtfillBuscarCodigo.getText()+c);
        int tamañoNumero = Digitos(numeroEscrito);
        Set<Integer> Codigos = GestionEstudiantes.getMapa().keySet();
        
        ModeloListaCodigos.removeAllElements();
        if(!("".equals(txtfillBuscarCodigo.getText()+c))){
            for(Integer codigo: Codigos){
                if(UltimosDigitos(codigo,tamañoNumero) == numeroEscrito){
                    String NombreEstudiante = GestionEstudiantes.getMapa().get(codigo).getNombres();
                    ModeloListaCodigos.addElement(codigo+": "+NombreEstudiante);
                }
            }
        }
    }
    
    public void RefrescarListaCodigosDeled(){
        
        String numeroEscritoAntes = txtfillBuscarCodigo.getText();
        int tamañoNumeroAntes = numeroEscritoAntes.length();
        Set<Integer> Codigos = GestionEstudiantes.getMapa().keySet();
        
        ModeloListaCodigos.removeAllElements();
        
        if(tamañoNumeroAntes<=1){
            
            MostrarListaCodigos();
            
        }else{
            
            for(Integer codigo: Codigos){
                
                String numeroEscritoDespues = numeroEscritoAntes.substring(0, numeroEscritoAntes.length()-1);
                int tamañoNumeroDespues = tamañoNumeroAntes-1;
                
                if(UltimosDigitos(codigo,tamañoNumeroDespues) == Integer.parseInt(numeroEscritoDespues)){
                    String NombreEstudiante = GestionEstudiantes.getMapa().get(codigo).getNombres();
                    ModeloListaCodigos.addElement(codigo+": "+NombreEstudiante);
                }
                
            }
            
        }
    }
    
    public void MostrarListaCodigos(){
        Set<Integer> Codigos = GestionEstudiantes.getMapa().keySet();
        
        for(Integer codigo: Codigos){
                String NombreEstudiante = GestionEstudiantes.getMapa().get(codigo.intValue()).getNombres();
                ModeloListaCodigos.addElement(codigo.intValue()+": "+NombreEstudiante);
            }
    }
    
    private int Digitos(int numero){
        String numeroComoString = String.valueOf(numero);
        int cantidadDigitos = numeroComoString.length();
        
        return cantidadDigitos;
    }
    
    private int UltimosDigitos(int numero, int digitos){
        
        String numeroString = String.valueOf(numero);
        numeroString = numeroString.substring(0, digitos);
        int ultimosDigitos = Integer.parseInt(numeroString);
        
        return ultimosDigitos;
    }
